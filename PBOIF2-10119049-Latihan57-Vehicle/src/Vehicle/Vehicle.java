package Vehicle;
public class Vehicle {
    private String myBrand;
    private String myModel;

    public Vehicle() {

    }

    public String getBrand() {
        return myBrand;
    }

    public void setBrand(String Brand) {
        this.myBrand = Brand;
    }

    public String getModel() {
        return myModel;
    }

    public void setModel(String Model) {
        this.myModel = Model;
    }
    
    
}
